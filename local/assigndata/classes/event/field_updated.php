namespace local_assigndata\event;

defined('MOODLE_INTERNAL') || die();

/**
 * The field_updated event class.
 *
 * @package    local_assigndata
 * @copyright  2017 Enrique Castro @ ULPGC
 * @license    http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class field_updated extends \core\event\base {

    /**
     * Init method.
     *
     * @return void
     */
    protected function init() {
        $this->data['objecttable'] = 'local_assigndata_fields';
        $this->data['crud'] = 'u';
        $this->data['edulevel'] = self::LEVEL_OTHER;
    }

    /**
     * Return localised event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('eventfieldupdated', 'local_assigndata');
    }

    /**
     * Returns description of what happened.
     *
     * @return string
     */
    public function get_description() {
        return "The user with id '$this->userid' has updated field with id '$this->objectid' 
                for the assignment activity with course module id '$this->contextinstanceid'.";
    }

    /**
     * Get URL related to the action.
     *
     * @return \moodle_url
     */
    public function get_url() {
        return new \moodle_url('/local/assigndata/view.php', array('id' => $this->contextinstanceid));
    }

    /**
     * Custom validation.
     *
     * @throws \coding_exception when validation does not pass.
     * @return void
     */
    protected function validate_data() {
        parent::validate_data();

        if (!isset($this->other['fieldname'])) {
            throw new \coding_exception('The \'fieldname\' value must be set in other.');
        }

        if (!isset($this->other['assignment'])) {
            throw new \coding_exception('The \'assignment\' value must be set in other.');
        }
    }

    
    public static function get_objectid_mapping() {
        return array('db' => 'local_assigndata_fields', 'restore' => 'data_field');
    }

    
    public static function get_other_mapping() {
        $othermapped = array();
        $othermapped['assignment'] = array('db' => 'assign', 'restore' => 'assign');

        return $othermapped;
    }
}
